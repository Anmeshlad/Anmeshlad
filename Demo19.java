package ArrayDemos;

//Q. Write a program to print the addition of two matrices.

public class Demo19 {

	public static void main(String[] args) {
		int arr1[][]= {{1,2,3},{3,4,4}};
		for(int i=0;i<arr1.length;i++) {
			for(int j=0;j<arr1.length;j++) {
				System.out.println(arr1[i][j]);
			}
		}

	}

}
